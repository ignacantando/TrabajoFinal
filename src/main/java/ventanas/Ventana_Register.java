	@Override
	public void keyReleased(KeyEvent e) {
		if(jRadioButtonFisico.isSelected()) {
			if(jPasswordFieldContra.getText().isEmpty() || jTextFieldDni.getText().isEmpty())
				jButtonRegistrarse.setEnabled(false);
			else {
				jButtonRegistrarse.setEnabled(true);
			}
		}
		else {
			if(jPasswordFieldContra.getText().isEmpty() || jTextFieldId.getText().isEmpty())
				jButtonRegistrarse.setEnabled(false);
			else {
				jButtonRegistrarse.setEnabled(true);
			}
		}
	}

